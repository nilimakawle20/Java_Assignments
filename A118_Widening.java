/*WAP to convert int into double(widening)*/
package typecasting_in_java;

public class A118_Widening {

	public static void main(String[] args) {
		double d=87;              //implicit widening
		System.out.println(d);
		
		double d1=(double)60;     //explicit widening
		System.out.println(d1);

	}

}
