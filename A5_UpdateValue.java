package assignment.basicprograms;

public class A5_UpdateValue {
	public static void main(String[] args) {
		int price=500;
		System.out.println("The old price of this product was: "+price);
		price=700; //price updated from 500 to 700
		System.out.println("The new price of the same product is now : "+price);
	}
}
